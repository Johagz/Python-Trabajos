#Metodos de Instancia
#Definimos la clase 
class Perro:
    def __init__(self, raza, nombre):
        self.raza = raza
        self.nombre = nombre
#Metodo para mostrar informacion 

def mostrar(self):
    return f"Perro: {self.raza} {self.nombre}"
#crear un objeto de la clase perro
perro1 = Perro("mestiza", "canelita")
#Usar el metodo de objeto
    
print(perro1.mostrar())

