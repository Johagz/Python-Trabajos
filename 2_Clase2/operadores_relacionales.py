#Ejemplo de Operadores Relacionales
a=10
b=5

#igual a ==
resultado_igual= (a==b)
print(f"¿{a} es iagual a {b}? {resultado_igual}")

#No igual a !=
resultado_no_igual= (a!=b)
print(f"¿{a} es diferente de {b}? {resultado_no_igual}")

#Mayor que >
Mayor_que= (a>b)
print(f"¿{a} es mayor que {b}? {Mayor_que}")

#Menor que <
Menor_que= (a<b)
print(f"¿{a} es menor que {b}? {Menor_que}")

#Mayor o igual que >=
Mayor_o_igual= (a>=b)
print(f"¿{a} es mayor o igual que {b}? {Mayor_que}")

#Menor o igual que <=
Menor_o_igual= (a<=b)
print(f"¿{a} es menor o igual que {b}? {Menor_que}")