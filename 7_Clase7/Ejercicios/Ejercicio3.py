#Ejercicio de Sets y Operaciones Básicas
#Union de los conjuntos 
conjunto1={1,2,3}
conjunto2={3,4,5}
union=conjunto1.union(conjunto2)
print(f"Imprimo la union del conjunto 1 y conjunto 2: {union}")
#Intersección de los conjuntos
inter=conjunto1.intersection(conjunto2)
print(f"Imprimo la intersección del conjunto 1 y conjunto 2:{inter}")
#Diferencia entre los conjuntos
dife=conjunto1.difference(conjunto2)
print("Imprime la diferencia entre conjuntos:", dife)
